   			<h3>History</h3>
   			<p>
			The Seattle Band Map began as a mental-exercise in the fall of 2009 when Rachel Ratner sketched out the connections between local bands on a pad of paper. Quickly the map began to grow, and with the help of artist Keith Whiteman, the map was redrawn to gargantuan size and displayed at a local art gallery. Whiteman envisioned it as an interactive piece, and the duo invited viewers to add their own connections. The map continued gaining local interest, and in 2010, through generous funding by the <a href="http://www.seattle.gov/filmandmusic/">Mayors Office of Film and Music</a>, and <a href="http://www.seattle.gov/arts/">Mayors Office of Arts and Cultural Affairs</a>, the map was reprinted and showcased throughout Seattle neighborhoods and festivals. At 12 feet long with over 700 bands, the physical map is now temporarily on exhibit at the <a href="http://www.empmuseum.org/index.asp">Experience Music Project | Science Fiction Museum</a> in Seattle, WA.</p>
<p>
In 2011, with the help of University of Washington computer science student Golf Sinteppadon, the map was transformed into an interactive website, which gained national attention in <a href"http://www.wired.com/underwire/2011/03/seattle-band-map/?pid=2927&pageid=61042&viewall=true">Wired Magazine</a> and has been presented in international conferences such as the 2nd <a href="http://www.interdisciplinarythemes.org/conferences/index.php/C2/">International Conference of Culture, Society and Technology</a> in Vancouver, B.C and the <a href="http://side-creative.ncl.ac.uk/communities/symposium11/">Connected Communities Symposium</a> in Newcastle, UK. This has lead to increased academic interest in the band map as a unique means of using digital technology to at the same time illustrate and foster relationships within communities.
			</p>
